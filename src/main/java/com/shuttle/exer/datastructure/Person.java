package com.shuttle.exer.datastructure;

public class Person {

    private Long id;

    private String name;

    private Integer age;

    public Person(String name, Integer age) {
        this.id = System.currentTimeMillis() + (long) (Math.random() * 1000);
        this.name = name;
        this.age = age;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }

}
